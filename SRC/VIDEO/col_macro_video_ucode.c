/* Video Ucode Version: 3.03 */ 

/********************************************************************/
/* This version of microcode offers macroblock-based error recovery */
/* and supports all production version of Colorado                  */
/* It supports 3:2 pulldown and fixes the following problems:       */
/* - BSkyB Music Choice (channel 480) corrupted for a few seconds   */
/*    after changing to the channel.                                */
/* - Occasional hesitation after channel change.                    */
/* - Background still displayed with lower resolution when motion   */
/*   video is stopped.                                              */
/* - Bottom 2 bits of the GOP timecode register now indicate the    */
/*   type of picture last decoded (I/P/B).                          */
/* - Still decode keeps decoding until a new command is received    */
/*   rather than stopping on a sequence end.                        */
/********************************************************************/

/**************************************************************************************************************************/
