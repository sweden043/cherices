/*********************************************************************************************************/
/*   Microcode source change log:                                                                        */
/*                                                                                                       */
/*   0.01 - converted from HONDO 0.13. use new registers for IPB decode indication and buffer in display */
/*   0.02 - modified INITI,INITP commands to check for rewind case, removed constant chroma address      */
/*   0.03 -  check for out of bounds slice vertical position. added field_cmd in PAUSE.                  */
/*           added field_cmd at end of slowm.                                                            */
/*   0.04 - added a/v sync bit status                                                                    */
/*   0.05 - check for PLSS in FIFOCLR, added slice error handling for stills, removed power saving code  */
/*   0.06 - added ifdef for macroblock error concealment                                                 */
/*          need to streamline code to fit error concealment, if MACROBLOCK flag is set during           */
/*          compile, rom image size will exceed memory capacity                                          */
/*   0.07 - change cmd 0x48 to FIFOCLR, comment out command 0x45                                         */
/*          change IDLECMD to SNGLSTP                                                                    */
/*   0.08 - added RSTEM in STILCMD                                                                       */
/*   0.09 - added ifdefs for trickmode, allows room for macroblock error concealment                     */
/*   0.10 - check for NTSC mode in TBMM, use GFLG2 for field match                                       */
/*   0.11 - clear SYNC bit in ERROR, make sure in sync 3 frames before setting A/V sync bit              */
/*   0.12 - write to GEN_REG1 in host, indicate I or P picture decoded                                   */
/*   0.13 - fix clearing a/v sync bit in PAUSE                                                           */
/*   0.14 - change still image decoding to continuously decode pictures until new command posted         */
/*           version WABASH,NUM000,NUM014                                                                */
/*   0.15 - modification for the error concealment relating to the dummy sequence header                 */
/*   0.16 - code to improve channel change time, patch hole of ignoring dummy frame header               */
/*   0.17 - bounds checking of writes to frame buffer                                                    */
/*   0.18 - changed video start algorithm from finding picture header to finding slice header            */
/*   0.19 - changes width of size if HD stream; changes for faster channel change times                  */
/*   0.21 - fix the decoder hang when encountering attenuated/noisy input signals.			                 */
/*   0.22 - fix for declaring early sync 3 frames early for use with fast channel change.                */
/*   0.23 - check for a new command on sequence end to allow correct detection of end of still decode    */
/*   0.24 - fix for wrong anchor being used for remainder of seq when CONTINUE issued after a PAUSE cmd  */
/*   0.25 - fix for using field mode instead of frame mode for PAUSE cmd to stop inter-field flicker     */
/*********************************************************************************************************/

