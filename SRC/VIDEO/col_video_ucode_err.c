/* Microcode source change log:                                                                                           */
/*   0.0 - Initial version. Based upon vmc_nechesb.s29 version 0.1 Removed 3:2 pulldown local pts storage and             */
/*         user data w/a (hang condition).  Also moved AUD_DEC to start of play command                                   */
/*   0.1 - Reset the mute flag on the play command                                                                        */
/*                                                                                                                        */
/*   0.2 - Power management in between vsync's                                                                            */
/*   0.3 - Removed power management temporarily, using gen1flg for keeping track                                          */
/*         of decode buffer for software. Write size register with latest I,P,or B decoded.                               */
/*   0.4 - Put power management back in code, use BACK_FRAME register to track decode buffer                              */
/*   1.0 - New registers for Rev C implemented for fast forward                                                           */
/*   1.1 - fix for Rev C user data bug.  Added handling of seq err in stills                                              */
/*   1.2 - removed ffinit,ffcont routines replaced with skipb skipp. error handling in stills: go to idle, wait           */
/*   1.3 - Added tbmm check to fast forward decode                                                                        */
/*        for continue command                                                                                            */
/*   1.4 - Modified error concealment routine to reset datapath and look for sequence on every error.  BksyB test         */
/*         DTCrptUS_v10.trp was run with previous code and caused death to the box.  Also removed the field               */
/*         command from the bbemp routine to make Glennon happy with the DVtc720W_v21 test.                               */
/*   1.5 - Backed out the changes from 1.3 and modified to fix the crpt stream by altering the ERRSLC and                 */
/*         inserting additional WT4FULL commands in certain places. Also removed the field command in bbemp               */
/*   1.6 - inserted vsync interval before field cmd in slow motion. This fixes the drm mpeg enable                        */
/*         /disable.                                                                                                      */
/*   1.7 - add bit in size register to indicate top field first                                                           */
/*   1.8 - corrected polarity for top field first                                                                         */
/*   1.9 - modified dummy address for user data bug fix, this fixes corrupted macroblock in upper left corner of picture  */
/*   1.10 - add 0xf to VSIZE to accomodate small picture macroblock decode                                                */
/*   1.11 - added wait state in SEQ_HEAD before setting stride in motion comp                                             */
/*   1.12 - previous version hung on still, removed check for FABUSY,MCBUSY                                               */
/*          also added flags to clear vld fifo after PAUSE is issued                                                      */
/*   1.13 - removed idle command 0x48, replaced with vld fifo clear. reinstate code to check for buffer empty             */
/*          or new host command at the beginning of PLAYMAIN. Added case in ERROR to check for pending host               */
/*          command in case buffer is empty.                                                                              */
/*   1.14 - on pause, display anchor if stopped on B picture. Round up HSIZE for small picture decode.                    */
/*          BACK_FRAME was used incorrectly, fixed in this version.                                                       */
/*   1.15 - ????                                                                                                          */
/*   1.16 - Added delay before getting STC to avoid async problems in the host a/v sync logic - GETPTS routine updated.   */
/*   1.17 - changed initi,initp to constant buffer address. Made code efficiency changes                                  */
/*          to free up space                                                                                              */
/*   1.18 - extra nop after writing buf_in_disp to size reg, uncomment line that writes VSIZE to                          */
/*          size register                                                                                                 */
/*   1.19 - fixes related v1.17: corrected branching in initi, initp                                                      */
/*   1.20 - moved rounding up VSIZE code to correct spurious size change interrupts                                       */
/*   1.21 - move rounding up VSIZE code again                                                                             */
/*   1.22 - removed error slice concealment, error concealment will now work as follows:                                  */
/*          an error in any stratum will revert the display to the last decoded anchor frame.                             */
/*          vld will be directed to search for the next sequence header.                                                  */
/*          2 consecutive error-free anchor frames must be decoded before continuing normally.                            */
/*   1.23 - re-format IPB decode buffer indicators because software interrupt latentcies                                  */
/*          caused the wrong size value to be read after size change interrupts. The size register also                   */
/*          gets updated on a decode complete interrupt, which is probably when software actually reads it.               */
/*          New error concealment not included in this version.                                                           */
/*   1.24 - IPB format changes slated for v1.23 didn't get checked in. v1.23 still has old IPB buffer indicators.         */
/*          This version puts in changes for IPB indicators plus width register will get updated with actual              */
/*          size with the lower 3 bits masked off.                                                                        */
/*   1.25 - Change to error case: after searching for seq_header, skip B frames instead of searching for I frame          */
/*   1.26 - made chroma buffer address constant, re-do the IPB buffer indication for the first anchor frame decoded after */
/*          PLAY command                                                                                                  */
/*   1.27 - removed forward buffer chroma address calculation that should have removed when the constant luma/chroma      */
/*          address was introduced in 1.26                                                                                */
/*   1.28 - removed static chroma buffer address. software people didn't like it.                                         */
/*          created check for out of bounds slice vertical position. Found this problem in a badly encoded still picture. */
/*   1.29 - modified continue command to search for 2 anchors after pause command                                         */
/*   1.30 - boundary checking for fadd writes, removed ERRGOP section                                                     */
/*   2.00 - created a/v sync bit status in height register, removed some nop's in code to create some more room in code   */
/*   2.01 - problem with using DRM_TMP3 for a/v sync status. switched to using STLCMDFlg1 for tracking status             */
/*   2.02 - STLCMDFlg1 caused jerkiness, use SPUSYNC/SPUFLG instead                                                       */
/*   2.03 - check for host commands during error recovery                                                                 */
/*   2.04 - modifications for slow motion: bypass fadd bounds checking, send field_cmd during BBEMP                       */
/*          use STLCMDFlg1 for slow motion, changed error still handling to pause after error.                            */
/*          use SEQEFLg for indicating in frame based error concealment                                                   */
/*   2.05 - changed code to do SKIPs for I/P streams                                                                      */
/*   2.06 - added field_cmd at end of slowm                                                                               */
/*   2.07 - in TBMM changed MATCH flag to GFLG2 which ignores progressive scan case                                       */
/*          removed features from 2.03 and 2.05 until more testing is done                                                */
/*          added field_cmd in PAUSE, removed fabusy,mcbusy from ERR_RESET                                                */
/*   2.08 - put back fabusy,mcbusy in ERR_RESET                                                                           */
/*   2.09 - remove fabusy,mcbusy. increase timing loop in ERR_RESET                                                       */
/*   2.10 - add check in FIFOCLR to bypass if in startup condition                                                        */
/*   2.11 - in still image case in ERROR, add command to find sequence end                                                */
/*   2.12 - remove WT4FULL at TMP005, check for slice errors in stills, removed some nops to streamline code              */
/*   2.13 - put WT4FULL at CSTE32, prevents INT7 in RTL world still                                                       */
/*   2.14 - shorten PTSRPT by 1 field time                                                                                */
/*   2.15 - put nop's where needed for ZVIEW statements                                                                   */
/*          shorten slow-motion period by 1 frame time, removed power-saving code                                         */
/*   2.16 - update drm flags in NORDEC                                                                                    */
/*   2.17 - in still decode, continue decode if error encountered in picture header                                       */
/*   2.18 - ifdef for macroblock based error concealment                                                                  */
/*   6.22 - based on 2.18, added broken link flag                                                                         */
/*   6.23 - removed GEN0Flg references, replaced with seqeflg, DO NOT USE GEN0Flg!                                        */
/*          increase PTSRPT by 1 field time                                                                               */
/*   6.24 - pause on I frame                                                                                              */
/*   6.25 - change to continous still decoding until new command                                                          */
